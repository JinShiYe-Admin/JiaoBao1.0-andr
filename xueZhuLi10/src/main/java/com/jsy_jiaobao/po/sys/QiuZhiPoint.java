package com.jsy_jiaobao.po.sys;

public class QiuZhiPoint {
	private String JiaoBaoHao;// 教宝号
	private int Point;// 积分
	private int DelPoint;// 扣分
	private String RecDate;// 日期
	public String getJiaoBaoHao() {
		return JiaoBaoHao;
	}
	public void setJiaoBaoHao(String jiaoBaoHao) {
		JiaoBaoHao = jiaoBaoHao;
	}
	public int getPoint() {
		return Point;
	}
	public void setPoint(int point) {
		Point = point;
	}
	public int getDelPoint() {
		return DelPoint;
	}
	public void setDelPoint(int delPoint) {
		DelPoint = delPoint;
	}
	public String getRecDate() {
		return RecDate;
	}
	public void setRecDate(String recDate) {
		RecDate = recDate;
	}
}